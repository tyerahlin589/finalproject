def add_numbers(x, y):
    """ Add two numbers """
    result = x + y
    return result

def subtract_numbers(x, y):
    """ Subtract the second number from the first one """
    difference = x - y
    return difference

def multiply_numbers(x, y):
    """ Multiply two numbers """
    product = x * y
    return product

def divide_numbers(x, y):
    """ Divide one number by another """
    quotient = x / y
    return quotient
