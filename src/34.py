def square_root(n):
    """
    This function calculates and returns the square root of a given number n.
    
    Example usage:
    >>> square_root(9)
    3.0
    >>> square_root(25)
    5.0
    """
    return n ** 0.5

# Test the function with some data points
print(square_root(4))  # Expected output: 2.0
